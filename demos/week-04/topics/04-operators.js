// arithmetic operators
// +, -, *, /, %
let x = 12, y = 13;

console.log( x + y );
console.log( x - y );
console.log( x * y );
console.log( x / y );
console.log( x % y );
console.log( 12 ** 3 );

console.clear();

// relational operators
// <, > , <=, >=, ==, !=, ===, !==
console.log( 12 < 13 );
console.log( 1 == '1' ); // true! bad practice to use ==
console.log( 1 === '1' ); // false - use ===

console.clear();

// logical operators
// !, &&, ||
console.log( !true );
console.log( !false );
console.log( true && true ); // true
console.log( false && true ); // false

